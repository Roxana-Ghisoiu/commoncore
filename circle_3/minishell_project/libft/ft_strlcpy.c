/*The function ft_strlcpy is a safer and more
 controlled version of string copying.
It copies a string from a source (src) to a destination (dest),
ensuring that the destination string is properly null-terminated
and preventing buffer overflows.*/
#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	i;

	i = 0;
	if (size == 0)
	{
		while (src[i])
			i++;
		return (i);
	}
	while (i < size - 1 && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	if (i < size)
		dest[i] = '\0';
	while (src[i] != '\0')
		i++;
	return (i);
}
